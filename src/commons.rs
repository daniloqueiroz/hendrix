/// A `spin::Mutex` wrapper to be able to implement
/// traits for it.
/// This is needed cause Rust compiler doesn't permit
/// trait implementations for types defined in other crates.
pub struct Locked<A> {
    inner: spin::Mutex<A>,
}

impl<A> Locked<A> {
    pub const fn new(inner: A) -> Self {
        Locked {
            inner: spin::Mutex::new(inner),
        }
    }

    pub fn lock(&self) -> spin::MutexGuard<A> {
        self.inner.lock()
    }
}
